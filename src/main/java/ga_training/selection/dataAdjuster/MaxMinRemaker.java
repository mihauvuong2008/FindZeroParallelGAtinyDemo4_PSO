package ga_training.selection.dataAdjuster;

public class MaxMinRemaker {

	public double max;
	public double min;
	public double average;
	public double fakeAverage;
	public double[] arrayData;

	public double getMax() {
		return max;
	}

	public void setMax(double max) {
		this.max = max;
	}

	public double getMin() {
		return min;
	}

	public void setMin(double min) {
		this.min = min;
	}

	public double getAverage() {
		return average;
	}

	public void setAverage(double average) {
		this.average = average;
	}

	public double getFakeAverage() {
		return fakeAverage;
	}

	public void setFakeAverage(double fakeAverage) {
		this.fakeAverage = fakeAverage;
	}

	public double[] getArrayData() {
		return arrayData;
	}

	public void setArrayData(double[] arrayData) {
		this.arrayData = arrayData;
	}

}
